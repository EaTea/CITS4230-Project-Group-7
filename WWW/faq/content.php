			<section>
				<h2>I want to share one of my lists with a friend. How can I do this?</h2>
				<p>
					If you have a list you want to share with a friend, then all you need to do is go to your dashboard, click on the list you want to share, select "Edit List", and on the next screen, press the "Add Friends" button. Simply search for the name or username of the friend you want to share the list with, click on the target user, and then click "Confirm".
				</p>
			</section>
			<section>
				<h2>I've shared a list with a friend, and they can view the list, but I want to let them add and remove tasks too.</h2>
				<p>
					You've shared a list, but your friend can't do everything that they want to. This is because your friend hasn't been given permission to take these actions &#8211; you need to go to your dashboard, select the list you've shared, and click to edit it. You should be able to see a list of people who you've shared the list with. You can then allow or disallow someone from doing certain things (such as adding or removing tasks) by ticking or unticking the boxes next to their names.
				</p>
			</section>
			<section>
				<h2>How do I mark something as completed or remove it from my tasks?</h2>
				<p>
					It's pretty simple. If you go to your task viewer, you'll notice that there's a little drop-down icon. Click on it, and it'll bring up a little menu where you can choose to remove or mark a task as completed.
				</p>
			</section>
			<section>
				<h2>I want to store my to-do list offline. How do I use the offline synch?</h2>
				<p>
					All you have to do is go to your list, select "Edit List", and in the corresponding panel, you can select "Export to Local Computer". You'll be asked where you want to save the offline copy, and then, presto! Your to-do list will be saved for offline use.
				</p>
